package myPage;

public class ChangeInfoDTO {
	private String changeName;
	private String changeEmail;
	private String changePhone;
	private String changePw;
	private String confirmPw;
	
	public String getChangeName() {
		return changeName;
	}
	public void setChangeName(String changeName) {
		this.changeName = changeName;
	}
	public String getChangeEmail() {
		return changeEmail;
	}
	public void setChangeEmail(String changeEmail) {
		this.changeEmail = changeEmail;
	}
	public String getChangePhone() {
		return changePhone;
	}
	public void setChangePhone(String changePhone) {
		this.changePhone = changePhone;
	}
	public String getChangePw() {
		return changePw;
	}
	public void setChangePw(String changePw) {
		this.changePw = changePw;
	}
	public String getConfirmPw() {
		return confirmPw;
	}
	public void setConfirmPw(String confirmPw) {
		this.confirmPw = confirmPw;
	}
	
}
