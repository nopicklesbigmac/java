package operarors;

public class Ex01 {
	public static void main(String[] args) {
		int data1 = 9, data2 = 2;
		System.out.println(data1+" + "+data2+" = "+(data1 + data2));
		System.out.println(data1+" - "+data2+" = "+(data1 - data2));
		System.out.println(data1+" * "+data2+" = "+(data1 * data2));
		System.out.println(data1+" / "+data2+" = "+(data1 / data2)); // 몫
		System.out.println(data1+" % "+data2+" = "+(data1 % data2)); //나머지
		
	}
}
