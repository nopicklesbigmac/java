package variables;

public class Ex07 {
   public static void main(String[] args) {
      char ch = 'A';
      int num = 98;
      
      System.out.println("ch : " + ch);
      System.out.println("(int)ch : " + (int)ch);
      
      System.out.println("num : " + num);
      System.out.println("(char)num : " + (char)num);
   }

}